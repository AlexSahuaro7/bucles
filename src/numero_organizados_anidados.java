public class numero_organizados_anidados {
    public static void main(String[] args) {
        int i;
        int j;
        //primero voy a realizar el for para las tres filas que serian tres veces
        for (i=1; i<=3; i++){
            for (j=1; j<=3; j++){
                System.out.print(j + "");
            }
            System.out.println();
        }
    }
}
